    private void cargarConf(){
        //Cofiguración de la ventana
        setTitle("Agregar ruta");
        setIconImage(new ImageIcon(getClass().getResource("/Images/icono.png")).getImage());
        setLocationRelativeTo(null);
        
        //Configuración del tiempo 
        ArrayList<String> paraHoras = new ArrayList();
        for (int i = 0; i <= 12; i++) {
            if(i<=9){
                paraHoras.add("0"+i);
            }
            else{
                paraHoras.add(""+i);
            }
            
        }
        ArrayList<String> lista = new ArrayList();
        for (int i = 0; i < 60; i++) {
            if(i<=9){
                lista.add("0"+i);
            }
            else{
                lista.add(""+i);
            }
            
        }
        jComboBoxHoras.removeAllItems();
        for (String num : paraHoras) {
            jComboBoxHoras.addItem(num);
        }
        jComboBoxMinutos.removeAllItems();
        for (String num : lista) {
            jComboBoxMinutos.addItem(num);
        }
        jComboBoxSegundos.removeAllItems();
        for (String num : lista) {
            jComboBoxSegundos.addItem(num);
        }
    }
    
    private void cargarRuta(){
        jTextFieldAlias.setText(this.ruta.getAlias());
        jTextFieldCosto.setText(this.ruta.getCosto().toString());
        jTextFieldDescripcion.setText(this.ruta.getDescrip());
        if(this.ruta.getDisponibilidad()){
            jCheckBoxDispon.setSelected(true);
        }
        String hora = this.ruta.getTiempo().toString();
        String[] parts = hora.split(":");
        jComboBoxHoras.setSelectedItem(parts[0]);
        jComboBoxMinutos.setSelectedItem(parts[1]);
        jComboBoxSegundos.setSelectedItem(parts[2]);
        //String part2 = parts[1]; // 034556
    }
    
    public void asignarRuta(Ruta ruta){
        this.ruta = ruta;
        cargarRuta();
    }
