    public void guardar(){
        if(!"".equals(jTextFieldAlias.getText())){
            if(!"".equals(jTextFieldCosto.getText())){
                if(!"".equals(jTextFieldDescripcion.getText())){
                    try {
                        String alias = jTextFieldAlias.getText();
                        double costo = Double.parseDouble(jTextFieldCosto.getText());
                        boolean disponible = false;
                        if(jCheckBoxDispon.isSelected()==true){
                            disponible = true;
                        }
                        String descrip = jTextFieldDescripcion.getText();
                        String hora = jComboBoxHoras.getSelectedItem().toString();
                        String minutos = jComboBoxMinutos.getSelectedItem().toString();
                        String segundos = jComboBoxSegundos.getSelectedItem().toString();
                        Time tiempo = Time.valueOf(hora+":"+minutos+":"+segundos);
                        Ruta ruta = new Ruta(this.ruta.getIdruta(),alias, costo, disponible, descrip, tiempo, null, Timestamp.from(Instant.now()));
                        RutaOperaciones rutaop = new RutaOperaciones();
                        rutaop.guardar(Conexion.obtener(), ruta);
                        setVisible(false);
                        dispose();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Hubo un error: "+e);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Falta ingresar una descripción");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Falta ingresar un costo");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Falta ingresar un alias");
        }
    }
